package com.rk.rk_pesona_dunia.data;

public class Data_GetterSetter {
    private String namaNegara;
    private String rincianNegara;
    private int fotoBenderaNegara;

    private String namaDestinasiWisata;
    private String rincianDestinatiWisata;
    private int fotoDestinasiWisata;

    public String getNamaNegara() {
        return namaNegara;
    }

    public void setNamaNegara(String namaNegara) {
        this.namaNegara = namaNegara;
    }

    public String getRincianNegara() {
        return rincianNegara;
    }

    public void setRincianNegara(String rincianNegara) {
        this.rincianNegara = rincianNegara;
    }

    public int getFotoBenderaNegara() {
        return fotoBenderaNegara;
    }

    public void setFotoBenderaNegara(int fotoBenderaNegara) {
        this.fotoBenderaNegara = fotoBenderaNegara;
    }


    public String getNamaDestinasiWisata() {
        return namaDestinasiWisata;
    }

    public void setNamaDestinasiWisata(String namaDestinasiWisata) {
        this.namaDestinasiWisata = namaDestinasiWisata;
    }

    public String getRincianDestinatiWisata() {
        return rincianDestinatiWisata;
    }

    public void setRincianDestinatiWisata(String rincianDestinatiWisata) {
        this.rincianDestinatiWisata = rincianDestinatiWisata;
    }

    public int getFotoDestinasiWisata() {
        return fotoDestinasiWisata;
    }

    public void setFotoDestinasiWisata(int fotoDestinasiWisata) {
        this.fotoDestinasiWisata = fotoDestinasiWisata;
    }

}
